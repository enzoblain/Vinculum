use crate::build::parser::Function;
use crate::build::utils::helpers::to_snake_case;

use std::io;
use std::path::{PathBuf, Path};
use std::{env, fs};
use std::io::Write;

pub(crate) fn generate_functions_with_modules(file_modules: &[(String, Vec<Function>)]) {
    let manifest_dir =
        PathBuf::from(env::var("CARGO_MANIFEST_DIR").expect("CARGO_MANIFEST_DIR is not set"));

    ensure_vinculum_declared(&manifest_dir)
        .expect("Failed to update lib.rs/main.rs");

    let dest = manifest_dir.join("src").join("vinculum");
    let dest_module = dest.join("mod.rs");

    fs::create_dir_all(&dest).expect("Failed to create destination directory");

    let mut mod_rs = String::new();
    mod_rs.push_str("// This file is auto-generated by build.rs\n");
    mod_rs.push_str("// Do not edit manually - changes will be overwritten during build\n\n");

    for (module_name, functions) in file_modules {
        let rust_module_name = to_snake_case(module_name);
        let module_file = dest.join(format!("{}.rs", rust_module_name));

        let mut code = String::new();
        code.push_str("// This file is auto-generated by build.rs\n");
        code.push_str("// Do not edit manually - changes will be overwritten during build\n\n");
        code.push_str("use vinculum_hs::ffi::call::call_haskell_typed;\n");
        code.push_str("#[allow(unused_imports)]\n");
        code.push_str("use vinculum_hs::ffi::value::{AcceptedTypes, Value};\n\n");

        for function in functions {
            code.push_str(&generate_function(function, module_name));
            code.push('\n');
            code.push('\n');
        }

        fs::write(&module_file, code)
            .unwrap_or_else(|e| panic!("Failed to write module file {:?}: {}", module_file, e));

        mod_rs.push_str(&format!("pub(crate) mod {};\n", rust_module_name));
    }

    fs::write(&dest_module, mod_rs).expect("Failed to write mod.rs");
}

fn generate_function(function: &Function, module_name: &str) -> String {
    let type_param = if function.generics.is_empty() {
        "()"
    } else {
        "T"
    };

    let args_sig = function
        .args
        .iter()
        .map(|arg| format!("{}: {}", arg.name, arg.r#type.rust_name()))
        .collect::<Vec<_>>()
        .join(", ");

    let args_values = function
        .args
        .iter()
        .map(|arg| arg.r#type.to_rust_value_with_param(&arg.name, type_param))
        .collect::<Vec<_>>()
        .join(", ");

    let generics = if function.generics.is_empty() {
        "".to_string()
    } else {
        format!(
            "<{}: 'static + AcceptedTypes>",
            function.generics.join(": Any + Clone + 'static, ")
        )
        .trim_start_matches(", ")
        .into()
    };
    let return_type = function.r#return.rust_name();
    let qualified_name = format!("{}_{}", to_snake_case(module_name), function.name);
    let description_comments = function
        .description
        .iter()
        .map(|desc| format!("/// {}", desc))
        .collect::<Vec<_>>()
        .join("\n");

    format!(
        "{}#[allow(non_snake_case, dead_code)]
pub fn {name}{generics}({args_sig}) -> {return_type} {{
    call_haskell_typed(\"{qualified_name}\", &[{args_values}])
        .{conversion}.expect(\"internal FFI type error: invalid return type\")
}}",
        if description_comments.is_empty() {
            String::new()
        } else {
            format!("{}\n", description_comments)
        },
        name = function.name,
        generics = generics,
        args_sig = args_sig,
        return_type = return_type,
        qualified_name = qualified_name,
        args_values = args_values,
        conversion = function.r#return.rust_return_conversion(),
    )
}

pub(crate) fn ensure_vinculum_declared(manifest_dir: &Path) -> io::Result<()> {
    let lib_path = manifest_dir.join("src").join("lib.rs");
    let main_path = manifest_dir.join("src").join("main.rs");

    let (target, is_lib) = if lib_path.exists() {
        (lib_path, true)
    } else {
        (main_path, false)
    };

    if !target.exists() {
        return Err(io::Error::new(
            io::ErrorKind::NotFound,
            "Neither lib.rs nor main.rs found",
        ));
    }

    let mut content = fs::read_to_string(&target)?;

    if content.contains("mod vinculum") {
        return Ok(());
    }

    let line = if is_lib {
        "pub(crate) mod vinculum;\n"
    } else {
        "mod vinculum;\n"
    };

    if is_lib {
        if !content.ends_with('\n') {
            content.push('\n');
        }
        content.push_str(line);
    } else {
        content = format!("{}\n{}", line, content);
    }

    let mut file = fs::File::create(&target)?;
    file.write_all(content.as_bytes())?;

    Ok(())
}